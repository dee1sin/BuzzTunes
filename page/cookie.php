<h3>What are cookies?</h3>
<p>Cookies are small text files that are placed on your computer by websites that you visit. 
    These text files can be read by these websites and help to identify you when you return to a website. 
    Cookies can be "persistent" or "session" cookies. Persistent cookies remain on your computer when you have gone offline, while session cookies are deleted as soon as you close your web browser.
</p>
